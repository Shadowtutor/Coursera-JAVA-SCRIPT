var greet = 'Hello, ';
console.log(greet.length);

/*O retorno de greet.length será 7 porque a string "Hello, " contém os seguintes caracteres:

1 H
2 e
3 l
4 l
5 o
6 , (vírgula)
7 (espaço)
Cada um desses caracteres é contado pela propriedade .length. 
Então, mesmo o espaço no final é considerado na contagem, somando um total de 7 caracteres.*/